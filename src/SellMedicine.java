    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String noOfUnit = jTextField6.getText();
        String uniqueId = jTextField2.getText();

        if (!noOfUnit.equals("") && !uniqueId.equals("")) {
            String name = jTextField3.getText();
            String companyName = jTextField4.getText();
            String pricePerUnit = jTextField5.getText();
            String totalPrice = jTextField7.getText();
            int checkStock = 0;
            int checkMedicineAlreadyExistInCart = 0;

            try {
                Connection con = ConnectionProvider.getCon();
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select *from medicine where medicine_id=" + uniqueId + "");
                while (rs.next()) {
                    if (rs.getInt("quantity") >= Integer.parseInt(noOfUnit)) {
                        checkStock = 1;
                    } else {
                        JOptionPane.showMessageDialog(null, "MEDICINE IS OUT OF STOCK" + rs.getInt("quantity") + "LEFT");
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            if (checkStock == 1) {
                DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
                if (jTable2.getRowCount() != 0) {
                    for (int i = 0; i < jTable2.getRowCount(); i++) {
                        if (Integer.parseInt(dtm.getValueAt(i, 0).toString()) == Integer.parseInt(uniqueId)) {
                            checkMedicineAlreadyExistInCart = 1;
                            JOptionPane.showMessageDialog(null, "MEDICINE ALREADY EXIST IN CART");
                        }
                    }
                }
                if (checkMedicineAlreadyExistInCart == 0) {
                    dtm.addRow(new Object[]{uniqueId, name, companyName, pricePerUnit, noOfUnit, totalPrice});
                    finalTotalPrice = finalTotalPrice + Integer.parseInt(totalPrice);
                    jLabel10.setText(String.valueOf(finalTotalPrice));
                    JOptionPane.showMessageDialog(null, "ADDED SUCCESSFULLY");
                }
                clearMedicineField();
            }
        } else {
            JOptionPane.showMessageDialog(null, "NUMBER OF UNITS AND MEDICINE ID FIELD IS REQUIRED");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int index = jTable2.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "DO YOU WANT TO REMOVE THIS MEDICINE", "CONFIRM", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            TableModel model = jTable2.getModel();
            String total = model.getValueAt(index, 5).toString();
            finalTotalPrice = finalTotalPrice - Integer.parseInt(total);
            jLabel10.setText(String.valueOf(finalTotalPrice));
            ((DefaultTableModel) jTable2.getModel()).removeRow(index);
        }
    }//GEN-LAST:event_jTable2MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (finalTotalPrice != 0) {
            billId = getUniqueId("Bill-");
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            if (jTable2.getRowCount() != 0) {
                for (int i = 0; i < jTable2.getRowCount(); i++) {
                    try {
                        Connection con = ConnectionProvider.getCon();
                        Statement st = con.createStatement();
                        st.executeUpdate("update medicine set quantity= quantity-" + Integer.parseInt(dtm.getValueAt(i, 4).toString()) + " where medicine_id=" + Integer.parseInt(dtm.getValueAt(i, 0).toString()));
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(null, e);
                    }
                }
            }
            try {
                SimpleDateFormat myFormat = new SimpleDateFormat("dd-MM-yyyy");
                Calendar cal = Calendar.getInstance();
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into bill(billId,billDate,totalPaid,generatedBy) values (?,?,?,?)");
                ps.setString(1, billId);
                ps.setString(2, myFormat.format(cal.getTime()));
                ps.setInt(3, finalTotalPrice);
                ps.setString(4, username);
                ps.executeUpdate();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

