    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String nameOrUniqueId = model.getValueAt(index, 0).toString();
        String uniqueId[] = nameOrUniqueId.split("-", 0);
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from medicine where medicine_id=" + uniqueId[0] + "");
            while (rs.next()) {
                jTextField2.setText(uniqueId[0]);
                jTextField3.setText(rs.getString("name"));
                jTextField4.setText(rs.getString("company_name"));
                jTextField5.setText(rs.getString("price_per_unit"));
                jTextField6.setText("");
                jTextField7.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_jTable1MouseClicked

